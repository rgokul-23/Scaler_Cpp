#include<iostream>
using namespace std;
int main(){
    int a=10,b=3;
    cout<< (a>b) <<endl;    
    cout<< (a<b) <<endl;
    cout<< (a>=b) <<endl; //greater than or equal to
    cout<< (a<=b) <<endl; //lesser than or equal to
    cout<< (a==b) <<endl;
    cout<< (a!=b) <<endl;    


    // with Numbers
    cout<< (10>7)<<endl; 
    cout<< (7>10)<<endl;
    cout<< (7<10)<<endl;
    cout<< (10<7)<<endl;
    cout<< (10==7)<<endl;
    cout<< (10==10)<<endl;
    return 0;
}