#include<iostream> 
using namespace std;
int main(){
// Bitwise Operators
// bits on of number (integers, chars)
// & - bitwise and
// | - bitwise or
// ^ - bitwise xor
// ~ - bitwise 1's compliment
// << - left shift
// >> - right shift
    cout << (5&3)<<endl;
    cout << (5|3)<<endl;
    cout << (5^3)<<endl;
    cout << ~(5^3)<<endl;
    cout << (~0) <<endl;
return 0;
}